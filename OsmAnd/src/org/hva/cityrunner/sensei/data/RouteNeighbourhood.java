package org.hva.cityrunner.sensei.data;

public class RouteNeighbourhood {
	int id;
	String neighbourhood;
	int route_h;
	int route_v;
	int route_a;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getNeighbourhood() {
		return neighbourhood;
	}
	public void setNeighbourhood(String neighbourhood) {
		this.neighbourhood = neighbourhood;
	}
	public int getRoute_h() {
		return route_h;
	}
	public void setRoute_h(int route_h) {
		this.route_h = route_h;
	}
	public int getRoute_v() {
		return route_v;
	}
	public void setRoute_v(int route_v) {
		this.route_v = route_v;
	}
	public int getRoute_a() {
		return route_a;
	}
	public void setRoute_a(int route_a) {
		this.route_a = route_a;
	}
	
	
}
